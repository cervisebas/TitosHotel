    private void populateModel() {
        habitacionesTableModel = new HabitacionesTableModel();
        
        for (long i = 1; i < 200; i++) {
            habitacionesTableModel.addRow(new Habitaciones(
                    i,
                    i * 2,
                    "20",
                    "40",
                    200 * i
                )
            );
        }
    }

